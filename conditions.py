temp = 0

temp = int(input('Please Enter the current temperature: '))

if temp >= 90:
    print ('Wear Shorts')

elif temp >= 70:
    print ('Short Sleeves are fine')

elif temp >= 50:
    print ('Wear A Jacket')

elif temp >= 32:
    print ('Wear a heavy Coat')

elif temp < 32:
    print ('Stay Inside')
